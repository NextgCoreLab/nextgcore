pub struct ZucState { lfsr: [u32; 16], r1: u32, r2: u32, x: [u32; 4] }
impl ZucState { pub fn new() -> Self { Self { lfsr: [0; 16], r1: 0, r2: 0, x: [0; 4] } } }
impl Default for ZucState { fn default() -> Self { Self::new() } }

#[inline] fn add_m(a: u32, b: u32) -> u32 { let c = a.wrapping_add(b); (c & 0x7FFFFFFF).wrapping_add(c >> 31) }
#[inline] fn mul_by_pow2(x: u32, k: u32) -> u32 { ((x << k) | (x >> (31 - k))) & 0x7FFFFFFF }
#[inline] fn rot(a: u32, k: u32) -> u32 { (a << k) | (a >> (32 - k)) }
#[inline] fn l1(x: u32) -> u32 { x ^ rot(x, 2) ^ rot(x, 10) ^ rot(x, 18) ^ rot(x, 24) }
#[inline] fn l2(x: u32) -> u32 { x ^ rot(x, 8) ^ rot(x, 14) ^ rot(x, 22) ^ rot(x, 30) }
#[inline] fn make_u32(a: u8, b: u8, c: u8, d: u8) -> u32 { ((a as u32) << 24) | ((b as u32) << 16) | ((c as u32) << 8) | (d as u32) }
#[inline] fn make_u31(a: u8, b: u32, c: u8) -> u32 { ((a as u32) << 23) | ((b & 0x7FFF) << 8) | (c as u32) }

impl ZucState {
    fn lfsr_init(&mut self, u: u32) {
        let mut f = self.lfsr[0];
        f = add_m(f, mul_by_pow2(self.lfsr[0], 8));
        f = add_m(f, mul_by_pow2(self.lfsr[4], 20));
        f = add_m(f, mul_by_pow2(self.lfsr[10], 21));
        f = add_m(f, mul_by_pow2(self.lfsr[13], 17));
        f = add_m(f, mul_by_pow2(self.lfsr[15], 15));
        f = add_m(f, u);
        for i in 0..15 { self.lfsr[i] = self.lfsr[i + 1]; }
        self.lfsr[15] = f;
    }
    fn lfsr_work(&mut self) {
        let mut f = self.lfsr[0];
        f = add_m(f, mul_by_pow2(self.lfsr[0], 8));
        f = add_m(f, mul_by_pow2(self.lfsr[4], 20));
        f = add_m(f, mul_by_pow2(self.lfsr[10], 21));
        f = add_m(f, mul_by_pow2(self.lfsr[13], 17));
        f = add_m(f, mul_by_pow2(self.lfsr[15], 15));
        for i in 0..15 { self.lfsr[i] = self.lfsr[i + 1]; }
        self.lfsr[15] = f;
    }
    fn bit_reorg(&mut self) {
        self.x[0] = ((self.lfsr[15] & 0x7FFF8000) << 1) | (self.lfsr[14] & 0xFFFF);
        self.x[1] = ((self.lfsr[11] & 0xFFFF) << 16) | (self.lfsr[9] >> 15);
        self.x[2] = ((self.lfsr[7] & 0xFFFF) << 16) | (self.lfsr[5] >> 15);
        self.x[3] = ((self.lfsr[2] & 0xFFFF) << 16) | (self.lfsr[0] >> 15);
    }
    fn f(&mut self) -> u32 {
        let w = (self.x[0] ^ self.r1).wrapping_add(self.r2);
        let w1 = self.r1.wrapping_add(self.x[1]);
        let w2 = self.r2 ^ self.x[2];
        let u = l1((w1 << 16) | (w2 >> 16));
        let v = l2((w2 << 16) | (w1 >> 16));
        self.r1 = make_u32(S0[(u >> 24) as usize], S1[((u >> 16) & 0xFF) as usize],
                          S0[((u >> 8) & 0xFF) as usize], S1[(u & 0xFF) as usize]);
        self.r2 = make_u32(S0[(v >> 24) as usize], S1[((v >> 16) & 0xFF) as usize],
                          S0[((v >> 8) & 0xFF) as usize], S1[(v & 0xFF) as usize]);
        w
    }
    pub fn initialize(&mut self, k: &[u8; 16], iv: &[u8; 16]) {
        for i in 0..16 { self.lfsr[i] = make_u31(k[i], EK_D[i], iv[i]); }
        self.r1 = 0; self.r2 = 0;
        for _ in 0..32 { self.bit_reorg(); let w = self.f(); self.lfsr_init(w >> 1); }
    }
    pub fn generate_keystream(&mut self, ks: &mut [u32]) {
        self.bit_reorg(); self.f(); self.lfsr_work();
        for i in 0..ks.len() { self.bit_reorg(); ks[i] = self.f() ^ self.x[3]; self.lfsr_work(); }
    }
}

pub fn zuc_eea3(ck: &[u8; 16], count: u32, bearer: u32, dir: u32, len: u32, m: &[u8], c: &mut [u8]) {
    let l = len.div_ceil(32) as usize;
    let l8 = len.div_ceil(8) as usize;
    let lastbits = (8 - (len % 8)) % 8;
    let mut iv = [0u8; 16];
    iv[0] = (count >> 24) as u8; iv[1] = (count >> 16) as u8;
    iv[2] = (count >> 8) as u8; iv[3] = count as u8;
    iv[4] = ((bearer << 3) | ((dir & 1) << 2)) as u8 & 0xFC;
    iv[8] = iv[0]; iv[9] = iv[1]; iv[10] = iv[2]; iv[11] = iv[3];
    iv[12] = iv[4]; iv[13] = iv[5]; iv[14] = iv[6]; iv[15] = iv[7];
    let mut state = ZucState::new();
    state.initialize(ck, &iv);
    let mut z = vec![0u32; l];
    state.generate_keystream(&mut z);
    for i in 0..l8 { c[i] = m[i] ^ ((z[i / 4] >> ((3 - i % 4) * 8)) & 0xff) as u8; }
    if lastbits > 0 && l8 > 0 { c[l8 - 1] &= 0x100u16.wrapping_sub(1 << lastbits) as u8; }
}

fn get_word(data: &[u32], i: u32) -> u32 {
    let ti = i % 32; let idx = (i / 32) as usize;
    if ti == 0 { data[idx] } else { (data[idx] << ti) | (data.get(idx + 1).copied().unwrap_or(0) >> (32 - ti)) }
}
fn get_bit(data: &[u8], i: u32) -> u8 {
    let idx = (i / 8) as usize;
    if idx < data.len() && (data[idx] & (1 << (7 - (i % 8)))) != 0 { 1 } else { 0 }
}

pub fn zuc_eia3(ik: &[u8; 16], count: u32, bearer: u32, dir: u32, len: u32, m: &[u8]) -> u32 {
    let mut iv = [0u8; 16];
    iv[0] = (count >> 24) as u8; iv[1] = (count >> 16) as u8;
    iv[2] = (count >> 8) as u8; iv[3] = count as u8;
    iv[4] = ((bearer << 3) & 0xF8) as u8;
    iv[8] = iv[0] ^ (((dir & 1) << 7) as u8);
    iv[9] = iv[1]; iv[10] = iv[2]; iv[11] = iv[3];
    iv[12] = iv[4]; iv[14] = ((dir & 1) << 7) as u8;
    let n = len + 64; let l = n.div_ceil(32) as usize;
    let mut state = ZucState::new();
    state.initialize(ik, &iv);
    let mut z = vec![0u32; l];
    state.generate_keystream(&mut z);
    let mut t: u32 = 0;
    for i in 0..len { if get_bit(m, i) != 0 { t ^= get_word(&z, i); } }
    t ^= get_word(&z, len);
    t ^ z[l - 1]
}

